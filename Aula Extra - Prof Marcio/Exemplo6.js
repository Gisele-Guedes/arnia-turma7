/*
Expressões lógicas

uma expressão é inteira se seu resultado é um numero inteiro
uma expressão é lógica se seu resultado é um valor lógico, ou seja, verdadeiro ou falso

boolean = {true, false}
os operadores lógicos são:
&& - leia-se: resultado é verdadeiro se os operadores são verdadeiros
|| - leia-se ou: resultado é verdadeiro se um for verdadeiro
Os operadores de comparação ==, !=, < , <=, etc tem resultado verdadeiro ou falso.
! operador negação = transforma falso em verdadeiro e verdadeiro em falso

zero em boleano é falso
*/

console.log((3 > 2 && 2==1))
console.log((3 > 2) || (2==1))
console.log((!3>2))
